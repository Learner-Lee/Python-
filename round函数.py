# round(x, n)，精度
round(45.23, 0)  # =45.0
round(45.23, 1)  # =45.20
round(45.23, -1)  # =50.0

round(4.5)  # =4
round(4.50000001)  # =5
round(5.5)  # =6
# 5后面非零就进一
# 5后面为零就，偶数不变，奇数进一

print("round(80.23456, 2) : ", round(80.23456, 2))
print("round(100.000056, 3) : ", round(100.000056, 3))
print("round(-100.000056, 3) : ", round(-100.000056, 3))
